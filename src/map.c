void		save_map(t_tamere *e, int map[22][22])
{
	int		i;
	int		j;

	e->world_map = (int **)malloc(sizeof(int *) * 22);
	i = -1;
	while (++i < 22)
	{
		j = -1;
		e->world_map[i] = (int *)malloc(sizeof(int) * 22);
		while (++j < 22)
			e->world_map[i][j] = map[i][j];
	}
}

